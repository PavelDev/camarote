void WorldSession::HandleReforgeItemOpcode(WorldPacket& recvData)
{
    TC_LOG_DEBUG("network", "WORLD: Received CMSG_REFORGE_ITEM");

    uint32 slot, reforgeEntry;
    ObjectGuid guid;
    uint32 bag;
    Player* player = GetPlayer();

    recvData >> reforgeEntry >> bag >> slot;

    recvData.ReadBitSeq<3, 5, 6, 4, 1, 7, 2, 0>(guid);
    recvData.ReadByteSeq<2, 1, 7, 6, 5, 0, 3, 4>(guid);

    if (!player->GetNPCIfCanInteractWith(guid, UNIT_NPC_FLAG_REFORGER))
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - Unit (GUID: %u) not found or player can't interact with it.", GUID_LOPART(guid));
        SendReforgeResult(false);
        return;
    }

    Item* item = player->GetItemByPos(bag, slot);
    if (!item)
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - Player (Guid: %u Name: %s) tried to reforge an invalid/non-existant item.",
                     player->GetGUIDLow(), player->GetName().c_str());
        SendReforgeResult(false);
        return;
    }

    if (!reforgeEntry)
    {
        // Reset the item
        if (item->IsEquipped() && !item->IsBroken())
            player->ApplyReforgeEnchantment(item, false);

        item->SetDynamicUInt32Value(ITEM_DYNAMIC_MODIFIERS, 0, 0);
        item->RemoveFlag(ITEM_FIELD_MODIFIERS_MASK, 1);
        item->SetState(ITEM_CHANGED, player);
        SendReforgeResult(true);
        return;
    }

    ItemReforgeEntry const* stats = sItemReforgeStore.LookupEntry(reforgeEntry);
    if (!stats)
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - Player (Guid: %u Name: %s) tried to reforge an item with invalid reforge entry (%u).",
                     player->GetGUIDLow(), player->GetName().c_str(), reforgeEntry);
        SendReforgeResult(false);
        return;
    }

    if (!item->GetReforgableStat(ItemModType(stats->SourceStat)) || item->GetReforgableStat(ItemModType(stats->FinalStat))) // Cheating, you cant reforge to a stat that the item already has, nor reforge from a stat that the item does not have
    {
        SendReforgeResult(false);
        return;
    }

    if (!player->HasEnoughMoney(uint64(item->GetSpecialPrice()))) // cheating
    {
        SendReforgeResult(false);
        return;
    }

    if (item->GetDynamicUInt32Value(ITEM_DYNAMIC_MODIFIERS, 0) != 0)
    {
        SendReforgeResult(false);
        return;
    }

    player->ModifyMoney(-int64(item->GetSpecialPrice()));

    item->SetDynamicUInt32Value(ITEM_DYNAMIC_MODIFIERS, 0, reforgeEntry);
    item->SetFlag(ITEM_FIELD_MODIFIERS_MASK, 1);
    item->SetState(ITEM_CHANGED, player);

    SendReforgeResult(true);

    if (item->IsEquipped() && !item->IsBroken())
        player->ApplyReforgeEnchantment(item, true);
}

void WorldSession::HandleChangeCurrencyFlags(WorldPacket& recvPacket)
{
    uint32 currencyId, flags;

    // 0x4 - show on backpack. 0x8 - move to unused. Are 0x1 and 0x2 ever sent?
    recvPacket >> flags >> currencyId;

    if (GetPlayer())
        GetPlayer()->ModifyCurrencyFlags(currencyId, uint8(flags));
}

void WorldSession::SendItemUpgradeResult(bool success)
{
    WorldPacket data(SMSG_ITEM_UPGRADE_RESULT, 1);
    data.WriteBit(success);
    data.FlushBits();
    SendPacket(&data);
}

void WorldSession::HandleUpgradeItemOpcode(WorldPacket& recvData)
{
    TC_LOG_DEBUG("network", "WORLD: Received CMSG_UPGRADE_ITEM");

    ObjectGuid npcGuid;
    ObjectGuid itemGuid;
    Player* player = GetPlayer();

    uint32 item_slot = 0;
    uint32 upgradeEntry = 0;
    uint32 item_bag = 0;
    recvData >> item_slot >> upgradeEntry >> item_bag;

    recvData.ReadBitSeq<6>(npcGuid);
    recvData.ReadBitSeq<4, 3, 5>(itemGuid);
    recvData.ReadBitSeq<5>(npcGuid);
    recvData.ReadBitSeq<1>(itemGuid);
    recvData.ReadBitSeq<7, 3>(npcGuid);
    recvData.ReadBitSeq<6, 2>(itemGuid);
    recvData.ReadBitSeq<2, 4>(npcGuid);
    recvData.ReadBitSeq<7>(itemGuid);
    recvData.ReadBitSeq<1, 0>(npcGuid);
    recvData.ReadBitSeq<0>(itemGuid);

    recvData.ReadByteSeq<4, 5>(itemGuid);
    recvData.ReadByteSeq<3, 0>(npcGuid);
    recvData.ReadByteSeq<1, 3, 7>(itemGuid);
    recvData.ReadByteSeq<7, 6>(npcGuid);
    recvData.ReadByteSeq<2>(itemGuid);
    recvData.ReadByteSeq<1, 4, 5>(npcGuid);
    recvData.ReadByteSeq<0, 6>(itemGuid);
    recvData.ReadByteSeq<2>(npcGuid);

    if (!player->GetNPCIfCanInteractWithFlag2(npcGuid, UNIT_NPC_FLAG2_ITEM_UPGRADE))
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - Unit (GUID: %u) not found or player can't interact with it.", GUID_LOPART(npcGuid));
        SendItemUpgradeResult(false);
        return;
    }

    Item* item = player->GetItemByGuid(itemGuid);
    if (!item)
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - Item (GUID: %u) not found.", GUID_LOPART(itemGuid));
        SendItemUpgradeResult(false);
        return;
    }

    // Check if item guid is the same as item related to bag and slot
    if (Item* tempItem = player->GetItemByPos(item_bag, item_slot))
    {
        if (item != tempItem)
        {
            TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - Item (GUID: %u) not found.", GUID_LOPART(itemGuid));
            SendItemUpgradeResult(false);
            return;
        }
    }
    else
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - Item (GUID: %u) not found.", GUID_LOPART(itemGuid));
        SendItemUpgradeResult(false);
        return;
    }

    ItemUpgradeEntry const* itemUpEntry = sItemUpgradeStore.LookupEntry(upgradeEntry);
    if (!itemUpEntry)
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - ItemUpgradeEntry (%u) not found.", upgradeEntry);
        SendItemUpgradeResult(false);
        return;
    }

    // Check if player has enough currency
    if (!player->HasCurrency(itemUpEntry->currencyId, itemUpEntry->currencyCost))
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - Player has not enougth currency (ID: %u, Cost: %u) not found.", itemUpEntry->currencyId, itemUpEntry->currencyCost);
        SendItemUpgradeResult(false);
        return;
    }

    uint32 actualUpgrade = item->GetDynamicUInt32Value(ITEM_DYNAMIC_MODIFIERS, 2);
    if (actualUpgrade != itemUpEntry->prevItemUpgradeId)
    {
        TC_LOG_DEBUG("network", "WORLD: HandleReforgeItemOpcode - ItemUpgradeEntry (%u) is not related to this ItemUpgradePath (%u).", itemUpEntry->Id, actualUpgrade);
        SendItemUpgradeResult(false);
        return;
    }

    item->SetDynamicUInt32Value(ITEM_DYNAMIC_MODIFIERS, 2, itemUpEntry->Id);
    item->SetFlag(ITEM_FIELD_MODIFIERS_MASK, 0x1|0x2|0x4);
    item->SetState(ITEM_CHANGED, player);

    // Don't forget to remove currency cost
    SendItemUpgradeResult(true);

    if (item->IsEquipped())
        player->ApplyItemUpgrade(item, true);

    uint32 const flags = MODIFY_CURRENCY_NO_GUILD_PERKS | MODIFY_CURRENCY_NO_CAP_CHANGE;
    player->ModifyCurrency(itemUpEntry->currencyId, -int32(itemUpEntry->currencyCost), flags);
}

void WorldSession::HandleSetLootSpecialization(WorldPacket& recvData)
{
    TC_LOG_DEBUG("network", "WORLD: Received CMSG_SET_LOOT_SPECIALIZATION");

    uint32 lootSpecialisation;
    recvData >> lootSpecialisation;

    // client sends 0 when setting to current specialisation
    if (!lootSpecialisation)
    {
        GetPlayer()->SetLootSpecialisation(lootSpecialisation);
        return;
    }

    auto specialisationEntry = sChrSpecializationsStore.LookupEntry(lootSpecialisation);
    if (!specialisationEntry)
    {
        TC_LOG_DEBUG("network", "Player tried to set their loot specialisation to %u which doesn't exist!", lootSpecialisation);
        return;
    }

    if (GetPlayer()->getClass() != specialisationEntry->classId)
    {
        TC_LOG_DEBUG("network", "Player tried to set their loot specialisation to %u which isn't valid for their class!", lootSpecialisation);
        return;
    }

    GetPlayer()->SetLootSpecialisation(lootSpecialisation);
}
